--35. 상영관 테이블에서 해당 상영관에 예약된 기록이 하나도 없는 상영관의 영화제목을 조회하시오. (NOT EXISTS 사용)
--(풀이)
select 영화제목
from 상영관
where not exists (select 1 from 예약 where 상영관.극장번호 = 예약.극장번호 and 상영관.상영관번호 = 예약.상영관번호);
--36. 예약 테이블에서 같은 고객이 동일 날짜에 두 건 이상 예약한 고객번호와 날짜를 조회하시오.
--(풀이)
select 고객번호, 날짜
from 예약
group by 고객번호, 날짜
having count(고객번호)>=2;
--37. 극장 테이블에서 소속된 모든 상영관의 가격이 10000원 이상인 극장의 극장이름을 조회하시오. (NOT EXISTS 활용)
--(풀이)
select 극장이름
from 극장
where not exists (select 가격 from 상영관 where 극장.극장번호 = 상영관.극장번호 and 가격<10000);
--38. 고객 테이블에서 예약 테이블에 서로 다른 극장에 2곳 이상 예약한 고객의 이름을 조회하시오
--(풀이)
select 이름
from 고객, 예약
where 고객.고객번호 = 예약.고객번호
group by 이름
having count(distinct 극장번호)>=2;
--39. 상영관 테이블에서 같은 극장 내에서 좌석수가 가장 많은 상영관의 영화제목을 조회하시오.
--(풀이)
select 영화제목
from 상영관 a1
where 좌석수 = (select max(좌석수) from 상영관 a2 where a2.극장번호 = a1.극장번호);
--40. 고객 테이블에서 가장 최근 날짜에 예약한 고객의 이름을 조회하시오.
--(풀이)
select 이름
from 고객, 예약
where 고객.고객번호 = 예약.고객번호
and 날짜 = (select max(날짜) from 예약);